#!/usr/bin/env python3
"""Generate sass/moe_sfa_probe.sass — HW micro-validation of the QMMA.SF
*sfa* operand (A-side per-32 E8M0 scale), one warp, one QMMA.16832.

Everything the production kernels prove today runs through *sfb* (B-side
block-32 UE8M0, per-lane LOW byte scales the lane's column); *sfa* is fed
the neutral 0x7f7f7f7f. The MX-A8 activation format needs real per-row
bytes in sfa, so this probe pins down the (thread, byte-position) -> A-row
mapping empirically:

  A = all 1.0 (e4m3 0x38), B = all 1.0, sfb = neutral 127
  sfa(thread t) = bytes [64+t, 112+t, 160+t, 208+t]  (b0..b3, all < 255)
  => C[r, c] = 32 * 2^(v - 127) where v = the sfa byte the HW reads for
     row r. v's range identifies the BYTE POSITION, v - range_base the
     THREAD. One launch decides the whole map.

CUTLASS (cute/atom/mma_traits_sm120.hpp) says SFA (T32,V32)->(M16,K32)
shape ((2,2,8),32) stride ((8,0,1),16), i.e. row(t) = (t&1)*8 + (t>>2)
with threads t and t+2 redundant. The probe verifies this on silicon and
resolves which redundant thread wins and which register byte is live.
"""
import sys

OUT = []


def L(s=""):
    OUT.append(s)


L("// moe_sfa_probe — QMMA.SF sfa (A-side E8M0) thread/byte mapping probe")
L("// (GENERATED by gen/gen_moe_sfa_probe.py)")
L(".entry moe_sfa_probe")
L("    .reg R0-R63")
L("    .param u64 d_out")            # 32 lanes x 4 c-regs x 4 B = 512 B
L()
L("    S2R R30, SR_TID.X ;                      // lane")
L("    LDCU.64 UR4, c[0x0][0x358] ;")
L("    UMOV UR6, 0x0 ;")
L("    LDC.64 R20, c[0x0][0x380] ;              // d_out")
L("    SHF.L.U32 R0, R30, 0x4, RZ ;             // lane*16")
L("    IMAD.WIDE.U32 R20, R0, 0x1, R20 ;")
L()
L("    // A fragment quad = 1.0 e4m3 everywhere (rows 0..15, k 0..31)")
L("    MOV R4, 0x38383838 ;")
L("    MOV R5, 0x38383838 ;")
L("    MOV R6, 0x38383838 ;")
L("    MOV R7, 0x38383838 ;")
L("    // B fragment pair = 1.0 e4m3 (8 cols x k32)")
L("    MOV R32, 0x38383838 ;")
L("    MOV R33, 0x38383838 ;")
L("    // sfb neutral")
L("    MOV R26, 0x7f7f7f7f ;")
L("    // sfa = per-thread bytes [64+t, 112+t, 160+t, 208+t]")
L("    IADD3 R1, PT, PT, R30, 0x40, RZ ;        // 64+t")
L("    MOV R2, 0x01010101 ;")
L("    MOV R3, 0x90603000 ;")
L("    IMAD R28, R1, R2, R3 ;                   // replicate + per-byte bias")
L("    // acc = 0")
L("    MOV R40, RZ ;")
L("    MOV R41, RZ ;")
L("    MOV R42, RZ ;")
L("    MOV R43, RZ ;")
L()
L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R40, R4.ROW, R32.COL, R40, R28, R26, UR6 ;")
L()
L("    // c0..c3 -> d_out + lane*16")
L("    STG.E desc[UR4][R20.64], R40 ;")
L("    STG.E desc[UR4][R20.64+0x4], R41 ;")
L("    STG.E desc[UR4][R20.64+0x8], R42 ;")
L("    STG.E desc[UR4][R20.64+0xc], R43 ;")
L("    EXIT ;")

path = sys.argv[1] if len(sys.argv) > 1 else "/tmp/moe_sfa_probe.sass"
open(path, "w").write("\n".join(OUT) + "\n")
print(f"wrote {path} ({len(OUT)} lines)")
