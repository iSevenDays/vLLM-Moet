#!/usr/bin/env python3
"""Generate sass/moe_w4q_mm.sass — M<=4 GEMM over RADIX-5 split FP4 planes:
resident 2-bit base codes + a 2.5-bit/elem QUINTAL refinement plane =
BIT-EXACT FP4 (all 16 e2m1 nibbles, zeros included) at 5/8 of the
non-split slot bytes (1.6x experts/GiB; 1.7x over the base cache where
non-split slots also carry scale sections).

Encoding (moe_w2_planes.pack_quintal_fragment_major):
  digit(elem) = e2m1 mag idx (0..4) for the small class, mag idx - 5
  (0..2) for the big class; word w (4 elems) packs base-5:
  c = d0 + 5*d1 + 25*d2 + 125*d3 (<= 624, 10 bits). Per (nb, k64, lane)
  the 8 words form an 80-bit record: bytes 0-7 in the P8 section
  (32 lanes x 8 B), bytes 8-9 in the P2 section (32 lanes x 2 B) =>
  320 B per (nb, k64) block, both sections naturally aligned.

Decode per word (validated on HW by gen_moe_w4q_probe.py, bit-exact):
  c    = funnel-extract 10 bits from (P2:P8_hi:P8_lo)
  q    = (x * 0x334) >> 12            ; exact /5 for x < 1024 (brute-forced)
  d_i  = x - 5*q (IMAD negative imm)  ; 4 digits, 3 chained divisions
  sel  = d0|d1<<4|d2<<8|d3<<12 + 5*is_big(selC)   ; = e2m1 mag idx
  mag  = PRMT({0,.5,1,1.5}, sel, {2,3,4,6})       ; FULL e2m1 mag pool
  out  = mag | sign(selC)                         ; e4m3 negate = MSB
is_big from the base-code spread: code in {0,3} <=> ~(b0^b1) & 1.

Desc ABI: 64 B/pair {a, as, base, ref, bs, c, m_rows, pad} — identical to
moe_w4s_mm (ref points at the quintal plane). Geometry (grid, NWARP
split-K, smem reduce, bf16 epilogue) inherited verbatim from gen_moe_w4s.

a32 format rev: the As plane is read at PER-32 stride (f32 [rows,
K/32], desc `as` row stride (K/32)*4) with the fold once per k32 —
one cubin set serves host-selected activation groups {32, 64, 128}
per GEMM. The per-32 groups this rev was built to test were E2E-
falsified on GSM8K (details + measured table: gen_moe_w2.py);
production defaults are 128/128/ue8m0 = the exact a128 numerics on
these kernels.
"""

import os
import sys

K = int(sys.argv[2]) if len(sys.argv) > 2 else 4096


def _nwarp(k):
    nb = k // 128
    cap = 8 if k >= 1024 else 4
    for n in range(min(cap, nb), 0, -1):
        if nb % n == 0:
            return n
    return 1


NWARP = int(os.environ.get("MOEW4Q_NWARP", "0")) or _nwarp(K)
KSLICE = K // NWARP
assert KSLICE % 128 == 0
NTHR = NWARP * 32
SMEM = NWARP * 1024 + (2048 if NTHR > 256 else 0)
ACC0, ACC1 = 48, 52

OUT = []


def L(s=""):
    OUT.append(s)


L("// moe_w4q_mm — M<=4 GEMM, RADIX-5 split FP4: resident 2-bit base +")
L("// 2.5-bit quintal refinement plane, bit-exact e2m1 (GENERATED by")
L("// gen/gen_moe_w4q.py — edit the generator, not this file). Magic /5")
L("// digit decode -> full-pool PRMT; block-32 UE8M0 via QMMA.SF sfb;")
L("// A f32 scales folded per k32 (a32; a128 folded per kb128);")
L("// desc CTAID.Y, 64B ABI (w4s layout).")
L(".entry moe_w4q_mm")
L("    .reg R0-R95")
L("    .param u64 d_desc")        # [pairs][8] u64 table
L("    .param u32 ldb")           # K
L("    .param u32 n_k64")
L("    .param u32 ldc")           # N*2
L("    .param u32 n_kb128")       # K/128 (As row stride = n_kb128*4 B = K/32)
L(f"    .shared .align 16 smem[{SMEM}]")
L()
L("    // ---- prologue ----")
L("    S2R R80, SR_TID.X ;")
L("    LDCU.64 UR4, c[0x0][0x358] ;")
L("    UMOV UR6, 0x0 ;")
L("    S2R R81, SR_CTAID.X ;                    // n-block (16 rows)")
L("    S2R R92, SR_CTAID.Y ;                    // (expert, token-group) pair")
L("    LDC R82, c[0x0][0x388] ;                 // ldb = K")
L("    LDC R84, c[0x0][0x394] ;                 // n_kb128")
L("    SHF.R.U32.HI R85, RZ, 0x5, R80 ;         // wid")
L("    LOP3.LUT R86, R80, 0x1f, RZ, 0xc0, !PT ; // lane")
L("    SHF.R.U32.HI R87, RZ, 0x2, R86 ;         // g")
L("    LOP3.LUT R88, R86, 0x3, RZ, 0xc0, !PT ;  // t")
L("    SHF.L.U32 R89, R88, 0x2, RZ ;            // 4t")
L()
L("    // desc = d_desc + pair*64 ; {a, as, base, ref, bs, c, m_rows, pad}")
L("    LDC.64 R2, c[0x0][0x380] ;               // d_desc")
L("    IMAD R0, R92, 0x40, RZ ;")
L("    IMAD.WIDE.U32 R2, R0, 0x1, R2 ;")
L("    LDG.E.128 R4, desc[UR4][R2.64] ;         // a_lo,a_hi,as_lo,as_hi")
L("    LDG.E.128 R8, desc[UR4][R2.64+0x10] ;    // base_lo,hi, ref_lo,hi")
L("    LDG.E.128 R12, desc[UR4][R2.64+0x20] ;   // bs_lo,hi, c_lo,hi")
L("    LDG.E R16, desc[UR4][R2.64+0x30] ;       // m_rows")
L("    MOV R64, R4 ; MOV R65, R5 ;              // d_a")
L("    MOV R66, R6 ; MOV R67, R7 ;              // d_as")
L("    MOV R68, R8 ; MOV R69, R9 ;              // d_base (2-bit plane)")
L("    MOV R70, R10 ; MOV R71, R11 ;            // d_ref (quintal plane)")
L("    MOV R74, R12 ; MOV R75, R13 ;            // d_bs (scale plane)")
L("    MOV R76, R14 ; MOV R77, R15 ;            // d_c")
L("    MOV R79, R16 ;                           // m_rows")
L("    // pad pair (m_rows==0, moe_align filler): leave before any plane I/O")
L("    ISETP.EQ.AND P4, PT, R79, RZ, PT ;")
L("    @P4 EXIT ;")
L("    LDC R78, c[0x0][0x390] ;                 // ldc")
L()
L("    // P2: g < m_rows (A loads)")
L("    ISETP.LT.AND P2, PT, R87, R79, PT ;")
L()
L("    // base plane ptr (w2 layout: 256B per (nb,k64), + lane*8)")
L("    LDC R83, c[0x0][0x38c] ;                 // n_k64 = K/64")
L("    IMAD R0, R81, R83, RZ ;                  // nb * n_k64")
L(f"    IMAD R0, R85, {hex(KSLICE // 64)}, R0 ;  // + wid slice base (k64 units)")
L("    SHF.L.U32 R1, R0, 0x8, RZ ;              // *256B")
L("    SHF.L.U32 R2, R86, 0x3, RZ ;             // lane*8")
L("    IADD3 R1, PT, PT, R1, R2, RZ ;")
L("    IMAD.WIDE.U32 R8, R1, 0x1, R68 ;         // -> R8: base plane ptr")
L("    // quintal plane ptrs: 320B per (nb,k64) block:")
L("    //   P8 at +lane*8 ; P2 at +256 + lane*2")
L("    IMAD R1, R0, 0x140, RZ ;                 // blk*320")
L("    IADD3 R3, PT, PT, R1, R2, RZ ;           // + lane*8")
L("    IMAD.WIDE.U32 R70, R3, 0x1, R70 ;        // -> R70: P8 ptr")
L("    SHF.L.U32 R2, R86, 0x1, RZ ;             // lane*2")
L("    IADD3 R1, PT, PT, R1, R2, RZ ;")
L("    IADD3 R1, PT, PT, R1, 0x100, RZ ;        // + 256")
L("    IMAD.WIDE.U32 R72, R1, 0x1, R10 ;        // -> R72: P2 ptr (d_ref)")
L()
L("    // Bs plane ptr: ((nb*(K/32) + wid*(KSLICE/32))*16 + g")
L("    SHF.R.U32.HI R0, RZ, 0x5, R82 ;          // K/32")
L("    IMAD R1, R81, R0, RZ ;")
L(f"    IMAD R1, R85, {hex(KSLICE // 32)}, R1 ;")
L("    SHF.L.U32 R1, R1, 0x4, RZ ;              // *16")
L("    IADD3 R1, PT, PT, R1, R87, RZ ;          // + g")
L("    IMAD.WIDE.U32 R74, R1, 0x1, R74 ;        // -> R74: lane's sbyte ptr")
L()
L("    // A row ptr (row g, direct fp8)")
L("    IMAD.WIDE.U32 R64, R87, R82, R64 ;       // + g*K")
L("    IMAD.WIDE.U32 R64, R89, 0x1, R64 ;       // + 4t")
L(f"    IMAD R0, R85, {hex(KSLICE)}, RZ ;")
L("    IMAD.WIDE.U32 R64, R0, 0x1, R64 ;        // + wid K-slice")
L("    // As ptr (f32 [rows, K/32] — per-32 groups, 4x finer than a128)")
L("    SHF.L.U32 R0, R84, 0x4, RZ ;             // As row stride = n_kb128*16 = K/8")
L("    IMAD.WIDE.U32 R66, R87, R0, R66 ;        // + g*stride")
L(f"    IMAD.WIDE.U32 R66, R85, {hex(KSLICE // 8)}, R66 ;  // + wid slice")
L()
L("    // constants: pad A words zero, pools, neutral sfa, As regs zero, accs zero")
L("    MOV R28, 0x7f7f7f7f ;                    // sfa neutral")
for r in (5, 7, 21, 23, 4, 6, 20, 22, 30, 31, 40, 41, 44, 45):
    L(f"    MOV R{r}, RZ ;")
L("    MOV R27, 0x3c383000 ;                    // mag pool LO: 0,.5,1,1.5 e4m3")
L("    MOV R58, 0x4c484440 ;                    // mag pool HI: 2,3,4,6 e4m3")
L("    MOV R90, 0x00008080 ;                    // sign pool: codes 0,1 -> 80")
for r in list(range(ACC0, ACC0 + 4)) + list(range(ACC1, ACC1 + 4)):
    L(f"    MOV R{r}, RZ ;")
L()


def emit_spread(src, bit, dst):
    """byte (4x 2-bit fields) of `src` at `bit` -> nibble-stride selector
    word in R{dst} (the proven w2 spread: 4x LOP3 + 3x IMAD)."""
    if bit:
        L(f"    SHF.R.U32.HI R0, RZ, {hex(bit)}, {src} ;")
    else:
        L(f"    MOV R0, {src} ;")
    L("    LOP3.LUT R1, R0, 0x03, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R2, R0, 0x0c, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R3, R0, 0x30, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R0, R0, 0xc0, RZ, 0xc0, !PT ;")
    L("    IMAD R1, R2, 0x4, R1 ;")
    L("    IMAD R1, R3, 0x10, R1 ;")
    L(f"    IMAD R{dst}, R0, 0x40, R1 ;")


def emit_extract10(w):
    """bits [10w, 10w+10) of the 80-bit record (R16:R13:R12) -> R19."""
    b = 10 * w
    if b + 10 <= 32:                       # words 0..2: inside R12
        if b:
            L(f"    SHF.R.U32.HI R19, RZ, {hex(b)}, R12 ;")
            L("    LOP3.LUT R19, R19, 0x3ff, RZ, 0xc0, !PT ;")
        else:
            L("    LOP3.LUT R19, R12, 0x3ff, RZ, 0xc0, !PT ;")
    elif b < 32:                           # word 3: spans R12/R13
        L(f"    SHF.R.U32.HI R19, RZ, {hex(b)}, R12 ;")
        L(f"    SHF.L.U32 R2, R13, {hex(32 - b)}, RZ ;")
        L("    LOP3.LUT R19, R19, R2, RZ, 0xfc, !PT ;")
        L("    LOP3.LUT R19, R19, 0x3ff, RZ, 0xc0, !PT ;")
    elif b + 10 <= 64:                     # words 4,5: inside R13
        L(f"    SHF.R.U32.HI R19, RZ, {hex(b - 32)}, R13 ;")
        L("    LOP3.LUT R19, R19, 0x3ff, RZ, 0xc0, !PT ;")
    elif b < 64:                           # word 6: spans R13/R16 (P2)
        L(f"    SHF.R.U32.HI R19, RZ, {hex(b - 32)}, R13 ;")
        L(f"    SHF.L.U32 R2, R16, {hex(64 - b)}, RZ ;")
        L("    LOP3.LUT R19, R19, R2, RZ, 0xfc, !PT ;")
        L("    LOP3.LUT R19, R19, 0x3ff, RZ, 0xc0, !PT ;")
    else:                                  # word 7: inside R16 (P2)
        L(f"    SHF.R.U32.HI R19, RZ, {hex(b - 64)}, R16 ;")
        L("    LOP3.LUT R19, R19, 0x3ff, RZ, 0xc0, !PT ;")


def emit_word(bsrc, bit, dst):
    """decode 4 elems: base byte (`bsrc` at `bit`) + record word in R19
    -> e4m3 bytes in R{dst}. Clobbers R0-R3, R18, R19."""
    emit_spread(bsrc, bit, 18)              # selC -> R18 (nibble codes)
    L("    IMAD R0, R19, 0x334, RZ ;")
    L("    SHF.R.U32.HI R0, RZ, 0xc, R0 ;           // q0 = c/5")
    L("    IMAD R1, R0, 0xfffffffb, R19 ;           // d0 = c - 5*q0")
    L("    IMAD R2, R0, 0x334, RZ ;")
    L("    SHF.R.U32.HI R2, RZ, 0xc, R2 ;           // q1")
    L("    IMAD R3, R2, 0xfffffffb, R0 ;            // d1")
    L("    IMAD R19, R2, 0x334, RZ ;")
    L("    SHF.R.U32.HI R19, RZ, 0xc, R19 ;         // q2 = d3")
    L("    IMAD R0, R19, 0xfffffffb, R2 ;           // d2")
    L("    IMAD R1, R3, 0x10, R1 ;                  // sel |= d1<<4")
    L("    IMAD R1, R0, 0x100, R1 ;                 // sel |= d2<<8")
    L("    IMAD R1, R19, 0x1000, R1 ;               // sel |= d3<<12")
    L("    SHF.R.U32.HI R2, RZ, 0x1, R18 ;")
    L("    LOP3.LUT R2, R18, R2, RZ, 0x3c, !PT ;    // b0^b1 per nibble")
    L("    LOP3.LUT R2, R2, 0x1111, RZ, 0x0c, !PT ; // is_big = ~xor & 1")
    L("    IMAD R1, R2, 0x5, R1 ;                   // sel = e2m1 mag idx")
    L("    PRMT R2, R27, R1, R58 ;                  // mag (full e2m1 pool)")
    L("    PRMT R3, R90, R18, RZ ;                  // sign bytes (80/00)")
    L(f"    LOP3.LUT R{dst}, R2, R3, RZ, 0xfc, !PT ; // OR signs")


def emit_k64(j):
    """One k64: load 8B base + 10B record + 4 B-sbytes + 2 A f32 scales + A;
    decode; per k32: QMMA into kb-accs then FFMA fold into the masters
    (per-32 A groups -> the fold runs every k32, not every kb128)."""
    base_off = 0x100 * j            # 256B per k64 (base plane)
    ref_off = 0x140 * j             # 320B per k64 (quintal plane)
    aoff = 0x40 * j
    soff = 0x20 * j
    as_off = 8 * j                  # 2 f32 scales per k64
    L(f"    // ---- k64 {j} ----")
    L(f"    @P2 LDG.E R30, desc[UR4][R66.64+{hex(as_off)}] ;      // As[g, k32a]")
    L(f"    @P2 LDG.E R31, desc[UR4][R66.64+{hex(as_off + 4)}] ;  // As[g, k32b]")
    L(f"    LDG.E.64 R24, desc[UR4][R8.64+{hex(base_off)}] ;      // base codes 8B")
    L(f"    LDG.E.64 R12, desc[UR4][R70.64+{hex(ref_off)}] ;      // record 0-64")
    L(f"    LDG.E.U16 R16, desc[UR4][R72.64+{hex(ref_off)}] ;     // record 64-80")
    L(f"    LDG.E.U8 R26, desc[UR4][R74.64+{hex(soff)}] ;         // S[g, k32a]")
    L(f"    LDG.E.U8 R29, desc[UR4][R74.64+{hex(soff + 0x8)}] ;   // S[8+g, k32a]")
    L(f"    LDG.E.U8 R56, desc[UR4][R74.64+{hex(soff + 0x10)}] ;  // S[g, k32b]")
    L(f"    LDG.E.U8 R57, desc[UR4][R74.64+{hex(soff + 0x18)}] ;  // S[8+g, k32b]")
    L(f"    @P2 LDG.E R4, desc[UR4][R64.64+{hex(aoff)}] ;         // A w0 lo")
    L(f"    @P2 LDG.E R6, desc[UR4][R64.64+{hex(aoff + 0x10)}] ;  // A w2")
    L(f"    @P2 LDG.E R20, desc[UR4][R64.64+{hex(aoff + 0x20)}] ;")
    L(f"    @P2 LDG.E R22, desc[UR4][R64.64+{hex(aoff + 0x30)}] ;")
    L("    // radix-5 decode: 8 words -> R32..R39")
    for w, ((bsrc, bit), dst) in enumerate((
        (("R24", 0), 32), (("R24", 8), 33),
        (("R24", 16), 34), (("R24", 24), 35),
        (("R25", 0), 36), (("R25", 8), 37),
        (("R25", 16), 38), (("R25", 24), 39),
    )):
        emit_extract10(w)                   # c -> R19
        emit_word(bsrc, bit, dst)
    def fold(x):
        """masters += kb-acc * As[g, k32] (c0/c1 rows only: M<=4), acc reset."""
        L(f"    FFMA R48, R40, {x}, R48 ;")
        L(f"    FFMA R49, R41, {x}, R49 ;")
        L(f"    FFMA R52, R44, {x}, R52 ;")
        L(f"    FFMA R53, R45, {x}, R53 ;")
        L("    MOV R40, RZ ; MOV R41, RZ ;")
        L("    MOV R44, RZ ; MOV R45, RZ ;")

    L("    // QMMA + fold per k32 (sfb = lane's COLUMN row scale, tile-exact)")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R40, R4.ROW, R32.COL, R40, R28, R26, UR6 ;")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R44, R4.ROW, R36.COL, R44, R28, R29, UR6 ;")
    fold("R30")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R40, R20.ROW, R34.COL, R40, R28, R56, UR6 ;")
    L("    QMMA.SF.16832.F32.E4M3.E4M3.E8 R44, R20.ROW, R38.COL, R44, R28, R57, UR6 ;")
    fold("R31")


n_k64 = KSLICE // 64
L(f"    // ---- K march: {n_k64} x k64 ----")
for j in range(n_k64):
    emit_k64(j)
L()
L("    // ---- epilogue: smem split-K reduce + bf16 store (skinny lineage) ----")
L("    IMAD R0, R85, 0x400, RZ ;")
L("    SHF.L.U32 R1, R86, 0x4, RZ ;")
L("    IADD3 R0, PT, PT, R0, R1, RZ ;")
for i in range(4):
    L(f"    STS [R0+{hex(i * 4)}], R{ACC0 + i} ;")
for i in range(4):
    L(f"    STS [R0+{hex(0x200 + i * 4)}], R{ACC1 + i} ;")
L("    BAR.SYNC.DEFER_BLOCKING 0x0 ;")
L()


def emit_reduce_store(slot_base):
    sb = f"+{hex(slot_base)}" if slot_base else ""
    L(f"    // slot = tid{sb}")
    if slot_base:
        L(f"    IADD3 R19, PT, PT, R80, {hex(slot_base)}, RZ ;")
    else:
        L("    MOV R19, R80 ;")
    L("    LOP3.LUT R2, R19, 0x7f, RZ, 0xc0, !PT ;")
    L("    SHF.R.U32.HI R3, RZ, 0x7, R19 ;")
    L("    IMAD R4, R3, 0x200, RZ ;")
    L("    IMAD R4, R2, 0x4, R4 ;")
    L("    LDS R30, [R4] ;")
    for wid in range(1, NWARP):
        L(f"    IADD3 R5, PT, PT, R4, {hex(wid * 0x400)}, RZ ;")
        L("    LDS R21, [R5] ;")
        L("    FADD R30, R30, R21 ;")
    L("    SHF.R.U32.HI R20, RZ, 0x2, R2 ;")
    L("    LOP3.LUT R21, R2, 0x3, RZ, 0xc0, !PT ;")
    L("    SHF.R.U32.HI R22, RZ, 0x2, R20 ;")
    L("    SHF.R.U32.HI R23, RZ, 0x1, R21 ;")
    L("    IMAD R22, R23, 0x8, R22 ;")
    L("    LOP3.LUT R24, R20, 0x3, RZ, 0xc0, !PT ;")
    L("    LOP3.LUT R25, R21, 0x1, RZ, 0xc0, !PT ;")
    L("    IMAD R24, R24, 0x2, R25 ;")
    L("    SHF.L.U32 R26, R81, 0x4, RZ ;")
    L("    IMAD R26, R3, 0x8, R26 ;")
    L("    IADD3 R26, PT, PT, R26, R24, RZ ;")
    L("    SHFL.DOWN PT, R31, R30, 0x1, 0x1f ;")
    L("    F2FP.BF16.F32.PACK_AB R31, R31, R30 ;")
    L("    ISETP.LT.AND P1, PT, R22, R79, PT ;")
    L("    ISETP.EQ.AND P1, PT, R25, RZ, P1 ;")
    if NTHR > 256:
        L("    ISETP.LT.U32.AND P1, PT, R19, 0x100, P1 ;")
    L("    IMAD R14, R22, R78, RZ ;")
    L("    IMAD R14, R26, 0x2, R14 ;")
    L("    MOV R15, RZ ;")
    L("    IADD.64 R16, R14, R76 ;")
    L("    @P1 STG.E desc[UR4][R16.64], R31 ;")


for base in range(0, 256, min(NTHR, 256)):
    emit_reduce_store(base)
L("    EXIT ;")

path = sys.argv[1] if len(sys.argv) > 1 else "/tmp/moe_w4q_mm.sass"
open(path, "w").write("\n".join(OUT) + "\n")
print(f"wrote {path} ({len(OUT)} lines, K={K}, NWARP={NWARP})")
